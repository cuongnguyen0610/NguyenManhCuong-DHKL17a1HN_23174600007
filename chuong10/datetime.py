####Number
import math
print(abs(-2))
print(math.ceil(5.5)) #giá trị nguyên nhỏ nhất lớn hơn x
print(math.exp(4))  #e^x
print(math.fabs(-9)) # |x|
print(math.floor(6.25)) #giá trị nguyên lớn nhất nhỏ hơn x
print(math.log(5))  #log(x),x>0
print(max(20,52,15)) # max
print(min(23,5,15)) #min
print(math.modf(3.5)) # =0.5 , 3.0 
print(math.modf(10)) # = 0.0,10.0
print(math.pow(2,3))
print(round(12.346453736462,3)) # làm tròn với 3 số thập phân
print(math.sqrt(8)) 
